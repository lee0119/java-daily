public class for2 {
    public static void main(String[] args) {


        //구구단 안보고도 쓰기 
        for (int i = 1; i < 10; i++) {
            for (int j = 1; j < 10; j++) {
                System.out.println(i + "*" + j + "=" + (i * j) + " ");
            }
            System.out.println("");
        }
    }
}

