public class for2 {
    public static void main(String[] args) {

        //다양한 방법으로 구구단
        //오늘은 for문 
        for (int i = 2; i < 10; i++) {
            for (int j = 1; j < 10; j++) {
                System.out.println(i + "*" + j + "=" + (i * j) + " ");
            }
            System.out.println("");
        }
    }

}

