public class for2 {
    public static void main(String[] args) {

//생활코딩 Data &operation

        // 알고리즘 1-8
        for (int i = 2; i < 10; i++) {
            for (int j = 1; j < 10; j++) {
                System.out.println(i + "*" + j + "=" + (i * j) + " ");
            }
            System.out.println("");
        }
    }

}

